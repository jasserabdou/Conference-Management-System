/**
 * Enum representing different types of events.
 */
public enum EventType {
    /**
     * Represents a workshop event type.
     */
    WORKSHOP,

    /**
     * Represents a keynote event type.
     */
    KEYNOTE,

    /**
     * Represents a panel discussion event type.
     */
    PANEL_DISCUSSION,

    /**
     * Represents a seminar event type.
     */
    SEMINAR
}